3º Trimestre da gravidez: nascem os primeiros cabelos, engorda e prepara-se para nascer. Como se desenvolve o seu bebê entre a 27ª e a 40ª semana da gestação.
<h3 class="label-gestante">COMO SE DESENVOLVE O FETO NO 3º TRIMESTRE DE GRAVIDEZ</h3>
Durante o 3º trimestre, o bebê prepara-se para nascer. À medida que ganha peso, o corpo do bebê torna-se mais redondinho e a pele esticada. Ao nascer, a pele apresenta-se rosada e macia e as plantas das mãos e dos pés enrugadas.
<h3 class="label-gestante">O CORPO TORNA-SE PROPORCIONAL À CABEÇA</h3>
Os órgãos continuam a crescer, acompanhando o crescimento geral do corpo bebê e o tamanho da cabeça está cada vez mais proporcional ao corpo. Os sentidos apuram-se. O bebê torna-se sensível à luz, aos ruídos, ao gosto e cheiros.

<h3 class="label-gestante">O BEBÊ VIRA-SE DE CABEÇA PARA BAIXO PREPARANDO-SE PARA NASCER</h3>
Com a falta de espaço no útero, o bebê dobra as pernas, que ficam encolhidas sobre si mesmas (posição fetal) aumentando a pressão sobre a zona pélvica da mãe. O bebê poderá mudar de posição, virando-se de cabeça para baixo na direção da pélvis.

A íris torna-se colorida (a cor definitiva só aparece entre os 6 a 9 meses de idade, após a exposição dos olhos à luz natural) e as pupilas reagem à luz, sendo já capazes de distinguir a luz natural da artificial através da parede uterina.

Os intestinos acumulam uma considerável quantidade de mecónio (primeira matéria fecal produzida pelo bebê) que é usualmente eliminado logo após o parto.

<h3 class="label-gestante">SISTEMA IMUNITÁRIO</h3>
O sistema imunitário do bebê continuará a desenvolver-se depois do nascimento. Nos primeiros tempos, as defesas naturais do seu bebê serão reforçadas com a amamentação devido aos compostos que lhe transmite através do leite materno.

Na altura do nascimento, o bebê tem 270 ossos diminuindo para 206 na idade adulta uma vez que alguns deles se fundem para formar uma única estrutura óssea.

OS OSSOS DO CRÂNIO SÃO FLEXÍVEIS PARA FACILITAR A SAÍDA DA CABEÇA NO PARTO

O crânio ainda não é totalmente sólido, o que facilitará a passagem da cabeça do bebê pelo canal de parto. Como as 5 fontanelas (espaço macio e membranoso que separa os ossos do crânio) ainda são moles e estão separadas, é possível apertarem-se umas contra as outras durante o parto, alongando a cabeça do bebê. Após o parto, a cabeça retoma a sua forma natural.

Os pulmões estão completamente desenvolvidos e aumentam a produção de surfatante para manter os alvéolos abertos o que facilita o processo de respiração.

<h3 class="label-gestante">O BEBÊ ESTÁ PRONTO PARA NASCER</h3>
Às 37 semanas, o bebê é considerado um bebê de termo e pode nascer a qualquer momento. Todos os sistemas do bebê estão completamente desenvolvidos e prontos para a próxima etapa.

O coração bate a um ritmo de 110-150 pulsações por minuto. Quando respirar pela primeira vez, o sangue passará a correr pelos pulmões e o seu bebê tornar-se-á num ser humano autónomo, capaz de assegurar por si só todas as funções vitais!







