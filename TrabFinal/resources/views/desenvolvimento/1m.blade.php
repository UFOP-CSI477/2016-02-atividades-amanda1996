O primeiro trimestre da gravidez: um óvulo e um espermatozoide encontram-se e dão origem a uma nova vida. No final do 1º trimestre da gravidez, o seu bebê é um ser humano em ponto pequeno.
<h3 class="label-gestante" class="label-gestante">COMO SE DESENVOLVE O EMBRIÃO NO 1º TRIMESTRE DA GRAVIDEZ</h3>
Nas duas primeiras semanas de gravidez, não está realmente grávida. Como é extremamente improvável conseguir determinar o exato momento em que o espermatozoide (célula reprodutiva masculina) fertiliza o óvulo (célula reprodutiva feminina), <b>convencionou-se marcar o início da gravidez no primeiro dia do último período menstrual</b>.

<h3 class="label-gestante">O CICLO MENSTRUAL E A GRAVIDEZ</h3>
Quando o seu corpo inicia um novo ciclo menstrual (por norma, de 28 em 28 dias) reinicia, também, um novo ciclo de ovulação com vista à criação das condições necessárias para que ocorra uma fecundação.

<h3 class="label-gestante">FECUNDAÇÃO</h3>
O óvulo maduro vive entre 12 a 24 horas. Este é o tempo que tem para se encontrar com um espermatozoide e ser bem sucedido na sua missão. Se o óvulo não for fecundado, morre e é removido na próxima menstruação em conjunto com o revestimento da parede uterina.

Durante a sua idade fértil, o seu corpo vai repetir este processo todos os meses na expetativa de gerar uma nova vida.

Das poucas centenas de espermatozoides que conseguem alcançar as Trompas de Falópio e forçam a entrada no óvulo, apenas um (o mais forte e apto) o consegue perfurar e fertilizar.

O óvulo fertilizado tem agora toda a informação genética necessária para gerar uma vida nova. No momento da conceção, o sexo, as características físicas e eventuais problemas se saúde hereditários do bebê já foram determinados.

<h3 class="label-gestante">OFICIALMENTE GRÁVIDA!</h3>
No final da 2ª semana, com a conceção, está oficialmente grávida, apesar de ainda não o saber. Logo após a conceção, o seu corpo inicia uma série de alterações que o preparam para acolher e nutrir o bebê em formação.

Cerca de um dia após a fertilização, os núcleos do óvulo e do espermatozoide fundem-se. Cada progenitor contribui com 23 pares de cromossomas cujo núcleo guarda o código DNA de cada um dos pais. Os cromossomas juntam-se e dão origem a uma célula única (zigoto). Inicia-se agora a divisão celular, génese de uma nova vida.

<h3 class="label-gestante">O ÓVULO FERTILIZADO DESCE EM DIREÇÃO AO ÚTERO</h3>
Ao mesmo tempo que a divisão celular progride, o óvulo inicia a sua descida até ao útero, onde se irá implementar, e onde o bebê se irá desenvolver. Em apenas 6 dias, a célula inicial dividiu-se em cerca de 200 células e o embrião começa a sair da sua cápsula protetora. Abre-se e expande-se, fixando-se nas paredes do colo do útero.

Por volta da 4ª semana da gravidez, o embrião está firmemente implementado na parede uterina e tem a forma de um disco (disco embrionário) com cerca de 0,4 mm. Inicia-se a formação do tubo neural na camada da ectoderme e do sistema cardiovascular que, em pouco tempo, se tornará no cérebro e na coluna do embrião.

<h3 class="label-gestante">MARCOS DO DESENVOLVIMENTO DO EMBRIÃO NO 1º TRIMESTRE</h3>
Ao longo do 1º trimestre da gravidez, o feto cresce a um ritmo acelerado e desenvolve uma corrente sanguínea própria, os seus principais órgãos (coração, cérebro, fígado, pulmões e rins) e estruturas internas.

Se estiver grávida de uma menina, inicia-se o desenvolvimento dos ovários (quando a sua filha nascer já terá uma reserva de cerca de 2 milhões de óvulos) e, se for de um menino, inicia-se o desenvolvimento dos testículos que se manterão dentro da cavidade abdominal até ao final da gravidez, altura em que se deslocam para a bolsa escrotal.

<h3 class="label-gestante">CORPO E ÓRGÃOS EM FORMAÇÃO</h3>
Às 12 semanas, o coração está formado e funciona, bombeando sangue para todas as partes do corpo. Na ecografia da 12ª semana já o consegue ouvir a bater distintamente.

Na 13ª semana, os órgãos vitais do bebê – fígado, rins, intestinos, cérebro e pulmões – estão completamente formados e a funcionar (à exceção dos pulmões que ainda precisam de amadurecer as suas funções e que só funcionarão com o nascimento).

Os membros, as mãos, os pés e as articulações estão bem desenvolvidas e completamente definidas. O bebê possui já todos os órgãos, músculos, membros e ossos com que irá nascer. O bebê tem unhas na ponta dos pequenos dedos, já separados, os olhos e o nariz estão formados e distinguem-se claramente das restantes feições do rosto.

Ainda neste trimestre, inicia-se a formação dos dentes de leite (20). Apesar de os ouvidos só finalizarem o seu desenvolvimento à 24ª semana, o sentido de audição do bebê já lhe permite ouvir sons e vibrações do interior do corpo da mãe.

No final da 13ª semana, o bebê mede sensivelmente 10 cm da coroa aos pés e pesa aproximadamente 20 g. O seu bebê já consegue bocejar!

