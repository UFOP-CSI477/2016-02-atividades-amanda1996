O segundo trimestre da gravidez: os órgãos começam a funcionar e os sentidos desenvolvem-se a cada dia. O seu bebê entre a 14ª e a 26ª semanas da gestação.
<h3 class="label-gestante">COMO SE DESENVOLVE O FETO NO 2º TRIMESTRE DA GRAVIDEZ</h3>
<b>O bebê mede cerca de 35 cm da coroa aos pés e pesa aproximadamente 1000 g.</b>
Durante o 2º trimestre, o ritmo de crescimento do bebê acelera. Ao mesmo tempo que começa a acumular gordura, os órgãos internos prosseguem o seu processo de amadurecimento e exercitam as suas funções, preparando-se para a vida fora do útero.

O bebê tem um aspecto completamente humano e as feições do rosto do seu bebê estão praticamente definidas e pouco mudarão até ao nascimento!

<h3 class="label-gestante">O SISTEMA NERVOSO</h3>
<b>O cérebro do bebê começou a cresce muito rapidamente (crescimento que dura até os 5 anos de idade)</b>. Durante as próximas semanas, vai começar a sentir o seu bebê a mexer, primeiro de forma muito ténue, no final do trimestre, de forma vigorosa.

O sistema nervoso inicia o seu funcionamento, bebê movimenta-se e leva as mãos à boca. Os reflexos estão completamente operacionais e o bebê treina a deglutição engolindo e expelindo líquido amniótico.

<h3 class="label-gestante">INTESTINOS E PRIMEIRA MATÉRIA FECAL (MECÓNIO)</h3>
Os intestinos podem começar a produzir mecónio (primeira matéria fecal produzida pelo bebê, constituída por água, secreções gastrointestinais e outros produtos derivados da gestação que o bebê pode engolir) e os órgãos genitais externos estão formados.

<h3 class="label-gestante">LANUGEM: O CORPO COBRE-SE COM UMA FINA CAMADA DE PELOS</h3>
O corpo cobre-se progressivamente com lanugem (pelo muito fino) que protege a sua delicada pele do contacto permanente com o meio aquático do líquido amniótico. Os nervos cobrem-se por uma substância chamada mielina, que, ao isolar os nervos, permite uma perfeita coordenação dos movimentos dos membros.

<h3 class="label-gestante">SONO</h3>
O bebê desenvolve o seu próprio ritmo de sono, independente das suas horas de atividade e de descanso da mãe e dorme e acorda como um recém-nascido.

<h3 class="label-gestante">COM 24 SEMANAS O BEBÊ JÁ TEM HIPÓTESES DE SOBREVIVER FORA DO ÚTERO</h3>
Na ecografia da 20ª semana, já poderá ver o seu bebê completamente formado e a mexer-se vigorosamente no seu ventre. Já é possível observar o sexo do bebê, caso ele ajude e se posicione numa boa posição!

Às 24 semanas, o bebê é considerado viável, ou seja, com hipótese de sobreviver fora do útero. Ainda assim, teria que ser assistido numa unidade de cuidados intensivos neonatais.

<h3 class="label-gestante">OS ÓRGÃOS FUNCIONAM</h3>
Todos os órgãos principais do bebê já funcionam (exceto os pulmões, que continuam a amadurecer as suas funções até nascimento, momento em que começam a trabalhar. Até ao nascimento, o oxigénio de que necessita é-lhe fornecido através do cordão umbilical e da placenta).

Os seus pulmões ainda não estão suficientemente amadurecidos para cumprir em pleno a sua tarefa apesar de o bebê se preparar e praticar a respiração, inalando líquido amniótico: as narinas começam a abrir, nos pulmões, a maior parte das vias respiratórias e as bolsas de ar (alvéolos) estão desenvolvidas e continuam a produzir surfatante (líquido que atua nos alvéolos pulmonares e que assegura a elasticidade necessária para a função respiratória).

<h3 class="label-gestante">REFLEXOS PRIMÁRIOS</h3>
O reflexo de sucção está estabelecido e os dentes definitivos alojados no interior das gengivas (estes só descerão por volta dos 6 anos de idade, em substituição dos dentes primários).

As pestanas (cílios), as sobrancelhas e os olhos, que o bebê abre e fecha, estão completamente formados e são sensíveis à luz.

<h3 class="label-gestante">OS SENTIDOS DESENVOLVEM-SE</h3>
O cérebro continua a aperfeiçoar a atividade de ondas cerebrais para os sistemas visual e auditivo. Os ossos do ouvido médio (ouvido interno) continuam a endurecer, tornando possível a condução do som.

O bebê reconhece os sons produzidos pelo corpo da mãe, como a respiração, os batimentos cardíacos e a voz,movimenta-se ao ritmo da música e a pulsação cardíaca reage a estímulos auditivos externos, acelerando.

<h3 class="label-gestante">CURIOSIDADES</h3>
Se o seu bebê é uma menina, os ovários já contêm os ovócitos primários (ao nascer, a sua filha terá cerca de 500.000 ovócitos primários que vão morrendo gradualmente, atingindo os 30.000 na puberdade) e o útero está completamente formado.

Nos meninos, os testículos começam a descer da pélvis para o escroto e já contêm os primeiros espermatozóides imaturos.

